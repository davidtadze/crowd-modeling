class Color:
    def __init__(self, r=0, g=0, b=0):
        self._r = r
        self._g = g
        self._b = b

WHITE      = Color(255, 255, 255)
BLACK      = Color(  0,   0,   0)
RED        = Color(255,   0,   0)
GREEN      = Color(  0, 255,   0)
BLUE       = Color(  0,   0, 255)
GRAY       = Color(128, 128, 128)